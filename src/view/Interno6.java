    private void btFinalizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btFinalizarMouseClicked
        //Object[] dados = pegaCampos();

        if ((campoNome.getText().trim().isEmpty())
                || (campoCartao.getText().trim().isEmpty())
                || (campovalidade.getText().trim().isEmpty())
                || (campocvc.getText().trim().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha os Campos!");
        } else {

            String cpf = this.arrayListHospede.get(EscolherHospede.getSelectedIndex()).getCpf();

            for (int i = 0; i < this.arrayListHospede.size(); i++) {
                if (cpf.equals(this.arrayListHospede.get(i).getCpf())) {
                    this.arrayListHospede.remove(i);
                }
            }

            for (int i = 0; i < this.arrayListQuartos.size(); i++) {
                if (cpf.equals(this.arrayListQuartos.get(i).getHospede_id().getCpf())) {
                    this.arrayListQuartos.remove(i);
                    Quartos.adicionarVaga(i);
                }
            }

            for (int i = 0; i < this.arrayListAluguel.size(); i++) {
                if (cpf.equals(this.arrayListAluguel.get(i).getHospede_id().getCpf())) {
                    this.arrayListAluguel.remove(i);

                }
            }

            for (int i = 0; i < this.arrayListRestaurante.size(); i++) {
                if (cpf.equals(this.arrayListRestaurante.get(i).getHospede_id().getCpf())) {
                    this.arrayListRestaurante.remove(i);

                }
            }
            for (int i = 0; i < this.arrayListBabySitter.size(); i++) {
                if (cpf.equals(this.arrayListBabySitter.get(i).getHospede_id().getCpf())) {
                    this.arrayListBabySitter.remove(i);

                }
            }
            limparCampos();
            JOptionPane.showMessageDialog(null, "Contrato Finalizado");
        }
    }//GEN-LAST:event_btFinalizarMouseClicked

